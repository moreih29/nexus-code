#!/usr/bin/env node
// packages/shared/scripts/generate-metadata.mjs
// nexus-core metadata -> TypeScript 상수 생성기
// 실행: bun run generate  (packages/shared 기준)

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { dirname } from 'path';
import { fileURLToPath } from 'url';
import { createRequire } from 'module';

const __dirname = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

// require.resolve로 동적 탐지 — Bun hoisting 위치에 무관하게 동작
const manifestPath = require.resolve('@moreih29/nexus-core/manifest.json');
const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));

const OUTPUT_DIR = new URL('../src/generated/', import.meta.url).pathname;
mkdirSync(OUTPUT_DIR, { recursive: true });

// ── agents.ts 생성 ────────────────────────────────────────────────────────────

const agentsLines = [];
agentsLines.push('// Auto-generated by scripts/generate-metadata.mjs — DO NOT EDIT');
agentsLines.push(`// Source: @moreih29/nexus-core@${manifest.nexus_core_version}`);
agentsLines.push('');
agentsLines.push("export type AgentCategory = 'how' | 'do' | 'check';");
agentsLines.push("export type AgentResumeTier = 'persistent' | 'bounded' | 'ephemeral';");
agentsLines.push("export type AgentModelTier = 'high' | 'standard';");
agentsLines.push("export type AgentCapability = 'no_file_edit' | 'no_task_create' | 'no_task_update';");
agentsLines.push('');
agentsLines.push('export interface AgentMetadata {');
agentsLines.push('  id: string;');
agentsLines.push('  name: string;');
agentsLines.push('  alias_ko?: string;');
agentsLines.push('  description: string;');
agentsLines.push('  task?: string;');
agentsLines.push('  category: AgentCategory;');
agentsLines.push('  capabilities: AgentCapability[];');
agentsLines.push('  resume_tier: AgentResumeTier;');
agentsLines.push('  model_tier: AgentModelTier;');
agentsLines.push('}');
agentsLines.push('');

const agentEntries = manifest.agents.map((a) => {
  const fields = [];
  fields.push(`    id: ${JSON.stringify(a.id)},`);
  fields.push(`    name: ${JSON.stringify(a.name)},`);
  if (a.alias_ko !== undefined) {
    fields.push(`    alias_ko: ${JSON.stringify(a.alias_ko)},`);
  }
  fields.push(`    description: ${JSON.stringify(a.description)},`);
  if (a.task !== undefined) {
    fields.push(`    task: ${JSON.stringify(a.task)},`);
  }
  fields.push(`    category: ${JSON.stringify(a.category)},`);
  fields.push(`    capabilities: ${JSON.stringify(a.capabilities)},`);
  fields.push(`    resume_tier: ${JSON.stringify(a.resume_tier)},`);
  fields.push(`    model_tier: ${JSON.stringify(a.model_tier)},`);
  return `  {\n${fields.join('\n')}\n  }`;
});

agentsLines.push('export const AGENTS: readonly AgentMetadata[] = [');
agentsLines.push(agentEntries.join(',\n'));
agentsLines.push('] as const;');
agentsLines.push('');

writeFileSync(`${OUTPUT_DIR}agents.ts`, agentsLines.join('\n'), 'utf8');

// ── vocabulary.ts 생성 ────────────────────────────────────────────────────────

const vocabLines = [];
vocabLines.push('// Auto-generated by scripts/generate-metadata.mjs — DO NOT EDIT');
vocabLines.push(`// Source: @moreih29/nexus-core@${manifest.nexus_core_version}`);
vocabLines.push('');

// CapabilityMetadata
vocabLines.push('export interface CapabilityHarnessMapping {');
vocabLines.push('  claude_code: string[];');
vocabLines.push('  opencode: string[];');
vocabLines.push('}');
vocabLines.push('');
vocabLines.push('export interface CapabilityMetadata {');
vocabLines.push('  id: string;');
vocabLines.push('  description: string;');
vocabLines.push('  harness_mapping: CapabilityHarnessMapping;');
vocabLines.push('}');
vocabLines.push('');

const capEntries = manifest.vocabulary.capabilities.map((c) => {
  const hm = c.harness_mapping;
  return `  {\n    id: ${JSON.stringify(c.id)},\n    description: ${JSON.stringify(c.description)},\n    harness_mapping: {\n      claude_code: ${JSON.stringify(hm.claude_code)},\n      opencode: ${JSON.stringify(hm.opencode)},\n    },\n  }`;
});
vocabLines.push('export const CAPABILITIES: readonly CapabilityMetadata[] = [');
vocabLines.push(capEntries.join(',\n'));
vocabLines.push('] as const;');
vocabLines.push('');

// CategoryMetadata
vocabLines.push('export interface CategoryMetadata {');
vocabLines.push('  id: string;');
vocabLines.push('  description: string;');
vocabLines.push('}');
vocabLines.push('');

const catEntries = manifest.vocabulary.categories.map((c) =>
  `  { id: ${JSON.stringify(c.id)}, description: ${JSON.stringify(c.description)} }`
);
vocabLines.push('export const CATEGORIES: readonly CategoryMetadata[] = [');
vocabLines.push(catEntries.join(',\n'));
vocabLines.push('] as const;');
vocabLines.push('');

// ResumeTierMetadata
vocabLines.push('export interface ResumeTierMetadata {');
vocabLines.push('  id: string;');
vocabLines.push('  description: string;');
vocabLines.push('}');
vocabLines.push('');

const tierEntries = manifest.vocabulary.resume_tiers.map((t) =>
  `  { id: ${JSON.stringify(t.id)}, description: ${JSON.stringify(t.description)} }`
);
vocabLines.push('export const RESUME_TIERS: readonly ResumeTierMetadata[] = [');
vocabLines.push(tierEntries.join(',\n'));
vocabLines.push('] as const;');
vocabLines.push('');

// TagMetadata
vocabLines.push("export type TagType = 'skill' | 'inline_action';");
vocabLines.push('');
vocabLines.push('export interface TagMetadata {');
vocabLines.push('  id: string;');
vocabLines.push('  trigger: string;');
vocabLines.push('  type: TagType;');
vocabLines.push('  skill?: string;');
vocabLines.push('  handler?: string;');
vocabLines.push('  description: string;');
vocabLines.push('  variants?: string[];');
vocabLines.push('}');
vocabLines.push('');

const tagEntries = manifest.vocabulary.tags.map((t) => {
  const fields = [];
  fields.push(`    id: ${JSON.stringify(t.id)},`);
  fields.push(`    trigger: ${JSON.stringify(t.trigger)},`);
  fields.push(`    type: ${JSON.stringify(t.type)},`);
  if (t.skill !== undefined) fields.push(`    skill: ${JSON.stringify(t.skill)},`);
  if (t.handler !== undefined) fields.push(`    handler: ${JSON.stringify(t.handler)},`);
  fields.push(`    description: ${JSON.stringify(t.description)},`);
  if (t.variants !== undefined) fields.push(`    variants: ${JSON.stringify(t.variants)},`);
  return `  {\n${fields.join('\n')}\n  }`;
});
vocabLines.push('export const TAGS: readonly TagMetadata[] = [');
vocabLines.push(tagEntries.join(',\n'));
vocabLines.push('] as const;');
vocabLines.push('');

writeFileSync(`${OUTPUT_DIR}vocabulary.ts`, vocabLines.join('\n'), 'utf8');

console.log(`Generated:`);
console.log(`  ${OUTPUT_DIR}agents.ts`);
console.log(`  ${OUTPUT_DIR}vocabulary.ts`);
